//#include <stdio.h>
int	ft_atoi(char *str)
{
	int	nb;
	int	p;

	p = 0;
	nb = 0;
	while ((*str <= 13 && *str >= 9) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			p++;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		nb *= 10;
		nb += *str - 48;
		str++;
	}
	if (p % 2)
		return (-nb);
	return (nb);
}
